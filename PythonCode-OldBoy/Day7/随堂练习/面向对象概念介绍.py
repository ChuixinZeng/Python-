# 实例化：刚才讲的参数传到函数里面，出来一个对象，就是实例化。类经过实例化之后，才能进行调用。

# 封装：
# 1.防止数据被随意修改
# 2. 使外部程序不需要关注对象内部的构造，只需要通过此对象对外提供的接口进行直接访问即可

# 继承：通过父类——》子类的方式以最小代码量的方式，实现不同角色的共同点和不同点的同时存在

# 多态：
# 一个表示形式的多种状态。一个接口，多种实现。同一种事物表现出来的多种形态。
# 一个基类派生出不同的子类，每个子类在继承了父类中的同样的方法名的同时又对父类的方法做了不同的实现

